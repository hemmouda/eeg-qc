"""Determines the most important features per target type.
Specify the 10s any overlap no pp dataset"""

from sklearn.inspection import permutation_importance
from core.hgb_model import create_fresh_model
from core.dataset_reader import get_splits, prepare_splits_for
from core.consts import TARGETS

DATASET = "/Users/telos_matter/Desktop/uni/fha_semesters/bachelor/code/06 - tuar extraction/output/1-10s any overlap no preprocessing/10s"


def get_feature_importance():

    splits = get_splits(DATASET)

    results = {}

    for target_name, target_set in TARGETS.items():

        X_train, X_val, X_test, y_train, y_val, y_test = prepare_splits_for(
            splits, target_set
        )

        model = create_fresh_model()

        model.fit(X_train, y_train)

        result = permutation_importance(
            model,
            X_val,
            y_val,
            n_repeats=10,
            random_state=42,
            scoring="f1",
            n_jobs=-1,
        )

        importances = sorted(
            zip(X_train.columns, result.importances_mean),
            key=lambda x: x[1],
            reverse=True,
        )

        results[target_name] = {feature: float(score) for feature, score in importances}

    return results


